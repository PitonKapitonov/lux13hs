module top#(
//Because I use everywhere in cycles the limits '(max_number_rows + 1)' and '(max_number_columns + 1)' I have 25 lines each is 25 spots, 625 spots of 20x20 pixels each total.
    parameter  max_number_rows    = 24, 
    parameter  max_number_columns = 24 
)(
    input   logic           clk_in200_p,   // 200MHz oscillator
    input   logic           clk_in200_n, 
    //PHY
    input   logic           mii_tx_clk,
    output  logic [3:0]     mii_txd,    
    output  logic           mii_tx_en,   
    input   logic           mii_rx_clk,              
    input   logic [3:0]     mii_rxd,     
    input   logic           mii_rx_dv,           
    input   logic           mii_rx_er,             
    output  logic           refclk,       
    output  logic           mii_rst_n,
    
    //LUX data
    input logic [79:0]      CH_p,         //80 data channels from LUX13HS
    input logic [79:0]      CH_n,
    input logic             Pclk_p,
    input logic             Pclk_n,
    output logic            clk_lux,
    
   
   //LUX control signals
    output logic            st_readb_n,
    output logic            st_readt_n,
    output logic            st_rowb_n,
    output logic            st_rowt_n

);



//==============================================================================================================//
//                                                                                                              //
//                                         CLOCKING WIZARD                                                      //
//                                                                                                              //
//==============================================================================================================//

    logic clk, clk25, clk133, clk133_shifted, locked;
    assign refclk = clk25;
    
    logic clk_in200;
    IBUFDS IBUFDS_inst (
        .O(clk_in200),   // 1-bit output: Buffer output
        .I(clk_in200_p),   // 1-bit input: Diff_p buffer input (connect directly to top-level port)
        .IB(clk_in200_n)  // 1-bit input: Diff_n buffer input (connect directly to top-level port)
    );

    clk_wiz clk_wiz (
        .clk_out100(clk), 
        .clk_out25(clk25),
        .clk_out200(clk200),
        .clk_out300(clk300),
        .locked(locked),
        .clk_in(clk_in200)
    );
    
    clk_wiz_133 clk_wiz_133 (  
        .clk_out133(clk133),     
        .clk_out133_shifted(clk133_shifted),        //LUX real clock     
        .locked(),   
        .clk_in(clk_in200)       
    );
    
   //for sim
    clk_wiz_sim clk_wiz_sim (
        .clk_out_532_sim(clk_out_532_sim),     // data clcck for sim   
        .locked(),
        .clk_in1(clk_in200)    
    ); 
      


//==============================================================================================================//
//                                                                                                              //
//                                          UDP/Ethernet                                                        //
//                                                                                                              //
//==============================================================================================================//

 logic [7:0] s_axis_tdata_udp;

    UDP_Ethernet UDP_Ethernet (
        .locked     (locked),
        .clk        (clk),
        .mii_rst_n  (mii_rst_n),
        .mii_tx_clk (mii_tx_clk), 
        .mii_txd    (mii_txd), 
        .mii_tx_en  (mii_tx_en),  
        
        .mii_rx_clk (mii_rx_clk), 
        .mii_rxd    (mii_rxd), 
        .mii_rx_dv  (mii_rx_dv), 
        .mii_rx_er  (mii_rx_er),
        
        //UDP TX interface
        .udp_tx_tvalid(s_axis_tvalid_udp), 
        .udp_tx_tready(s_axis_tready_udp), 
        .udp_tx_tlast(s_axis_tlast_udp),  
        .udp_tx_tuser(udp_tx_tuser),
        .udp_tx_tdata(s_axis_tdata_udp),
        
        .clk_read_fifo(clk_read_fifo)
    );
    
    
   assign clk_lux = clk133;


//==============================================================================================================//
//                                                                                                              //
//                                         LUX13HS READOUT CONTROL                                              //
//                                                                                                              //
//==============================================================================================================//

    logic [8:0] row_address;
    LUX13HS_control LUX13HS_control (
        .clk133(Pclk),            //need to change to same clock domain that is 533MHz from LUX13HS_ADC_readout; now set_false_path_for this CDC is used
        .row_address(row_address), //from 0 to 431 that means 864 rows
        .st_rowb_n(st_rowb_n),
        .st_readb_n(st_readb_n),
        .st_rowt_n(st_rowt_n),
        .st_readt_n(st_readt_n),
        .TX_n(),
        .PD_n(),       
        .rst(!locked),
        .sending_data_active(sending_data_active)
    );
    
    

//==============================================================================================================//
//                                                                                                              //
//                                         LUX13HS ADC READOUT                                                  //
//                                                                                                              //
//==============================================================================================================//

 LUX13HS_ADC_readout LUX13HS_ADC_readout (
        .CH_p(CH_p),         //80 data channels from LUX13HS
        .CH_n(CH_n),
        .Pclk_p(Pclk_p),       // 133MHz frame (sample) clock from LUX13HS
        .Pclk_n(Pclk_n),
        .Pclk(Pclk),
        .row_address(row_address),    
        .st_readb_n(st_readb_n),
        .st_readt_n(st_readt_n),
        .st_rowb_n(st_rowb_n),
        .st_rowt_n(st_rowt_n),
        .clk200(clk200),
        .clk300(clk300),
        .rst(!locked),
        .sending_data_active(sending_data_active),
        .clk(clk),
        .s_axis_tdata_udp(s_axis_tdata_udp),
        .s_axis_tvalid_udp(s_axis_tvalid_udp),
        .s_axis_tlast_udp(s_axis_tlast_udp),
        .s_axis_tready_udp(s_axis_tready_udp),   //from UDP
        .clk_read_fifo(clk_read_fifo)
    );

endmodule
    
    
